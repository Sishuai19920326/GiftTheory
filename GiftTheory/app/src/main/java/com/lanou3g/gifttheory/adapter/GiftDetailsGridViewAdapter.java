import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;

import com.lanou3g.gifttheory.R;
import com.lanou3g.gifttheory.base.BaseViewHolder;
import com.lanou3g.gifttheory.bean.ListItemBean;

import java.util.List;

/**
 * Created by 司帅 on 17/2/25.
 */

public class GiftDetailsGridViewAdapter extends BaseAdapter{
    private List<ListItemBean.DataBean.ItemsBean.SpecsDomainsBean> specsDomainsBeanList;

    @Override
    public int getCount() {
        return specsDomainsBeanList == null ? 0 :specsDomainsBeanList.size();
    }

    @Override
    public Object getItem(int position) {
        return specsDomainsBeanList.get(position);
    }

    @Override
    public long getItemId(int position) {
        return position;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        BaseViewHolder baseViewHolder = BaseViewHolder.creatListViewHolder(convertView,parent,R.layout.popupwindow_gridview_item_gift);
        baseViewHolder.setText(R.id.rb_popupWindow_item,specsDomainsBeanList.get(0).getDomains().get(position));
        return baseViewHolder.getmView();
    }
}
