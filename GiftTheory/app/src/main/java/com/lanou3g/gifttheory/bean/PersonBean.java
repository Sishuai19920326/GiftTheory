import android.os.Parcel;
import android.os.Parcelable;

/**
 * Created by 司帅 on 17/3/3.
 */

public class PersonBean implements Parcelable{
    private String name;
    private String icon;
    private String sex;
    private String city;

    public PersonBean() {
    }

    protected PersonBean(Parcel in) {
        name = in.readString();
        icon = in.readString();
        sex = in.readString();
        city = in.readString();
    }

    public static final Creator<PersonBean> CREATOR = new Creator<PersonBean>() {
        @Override
        public PersonBean createFromParcel(Parcel in) {
            return new PersonBean(in);
        }

        @Override
        public PersonBean[] newArray(int size) {
            return new PersonBean[size];
        }
    };

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int flags) {
        dest.writeString(name);
        dest.writeString(icon);
        dest.writeString(sex);
        dest.writeString(city);
    }
}
